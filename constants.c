#include <stdio.h>
#include <stdlib.h>

// Checks user input and prints a message depending on whether the number input is even or odd.
int main()
{
    /* Constants */
    /* These cannot be modified, or else an error will be thrown */
    const int NUM = 5;
    const char myChar = 'a';
    const char myString[] = "My string!";
    printf("%d\n", NUM);
    printf("%d\n", NUM);
    printf("%c\n", myChar);
    printf("%s", myString);
    
    return 0;
}
